'''~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
- xが偶数
    - 前半の値 が答え
    - ただ 前半の値 > 後半の値 のとき 前半の値-1
- xが奇数
    - xをxの桁数-1個'9'が並んだ値に置き換える
    - xが偶数の時と同じように処理
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~'''

N = input()
dig = len(N)
if dig == 1:
    N, dig = 10, 2
elif dig % 2:
    N = int('9' * (dig-1))
else:
    N = int(N)
# 前半の値
f = N // (10 ** (dig//2))
# 後半の値
s = N - f * (10 ** (dig//2))
if f > s:
    print(f-1)
else:
    print(f)